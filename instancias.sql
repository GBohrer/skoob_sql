-- PROCEDURE

CREATE OR REPLACE PROCEDURE INSERE_GENERO_AUTOR( COD_LIVRO IN NUMBER , COD_GENERO IN NUMBER ) IS 

-- ESTA PROCEDURE INSERE O AUTOR PRINCIPAL. 
-- SE EXISTIR MAIS DE UM AUTOR PARA O LIVRO EM QUESTÃO 
-- VAI INSERIR APENAS O PRIMERIO REGISTRO DO AUTOR

  V_AUTOR NUMBER;

BEGIN

  BEGIN
     SELECT a.CODAUTOR 
      INTO V_AUTOR
       FROM CATALOGO_PUB A
          WHERE
            A.CODLIVRO  = cod_livro;
   EXCEPTION 
       WHEN NO_DATA_FOUND THEN 
         RAISE_APPLICATION_ERROR(-20010,'Nao existe autor cadastrado para o livro '|| cod_livro );
       WHEN TOO_MANY_ROWS THEN 
         NULL;  
   END;         
   

   BEGIN
     INSERT INTO GENEROSAUTOR( CODGENERO, CODAUTOR) VALUES( cod_genero , v_autor);
   EXCEPTION 
       WHEN DUP_VAL_ON_INDEX THEN NULL;
       WHEN OTHERS THEN 
          RAISE_APPLICATION_ERROR(-20020,'Erro na insercao tabela GENEROSAUTOR '|| SQLERRM );
   END; 

END;


-- TRIGGERS:

-- QUANDO UM NOVO GENERO É INSERIDO PARA UM LIVRO,
-- ESTA TRIGGER INSERE O MESMO GENERO PARA O AUTOR CUJO ESCREVEU O LIVRO EM QUESTÃO.
-- ELA CHAMA UMA PROCEDURE PARA IDENTIFICAR E INSERIR O GENERO AO AUTOR DO LIVRO.

CREATE OR REPLACE TRIGGER TRG_INSERE_GENERO_AUTOR
  AFTER INSERT ON GENEROSLIVRO
  FOR EACH ROW
  
BEGIN

  IF INSERTING THEN 

      INSERE_GENERO_AUTOR( :NEW.CODLIVRO, :NEW.CODGENERO);

  END IF;

END;
	

-- QUANDO UM USUARIO COMPLETA A LEITURA DE SEU LIVRO (INSERE OU MODIFICA SEU STATUS_LEITURA PARA "LIDO",
-- ESTA TRIGGER CONTABILIZA O NÚMERO DE PÁGINAS DO LIVRO LIDO NO PAGINOMETRO PESSOAL DO USUARIO.
CREATE OR REPLACE TRIGGER TRG_LEITURA_CONCLUIDA
  AFTER UPDATE OR INSERT ON ESTANTES
  FOR EACH ROW
  
  DECLARE
  V_NRO_PAGINAS NUMBER;

BEGIN

    IF UPPER(:NEW.STATUS_LEITURA) = 'LIDO' THEN 

          -- BUSCAR NUMERO DE PAGINAS NA TABELA DE LIVROS
          -- E GUARDA NA VARIAVEL V_NRO_PAGINAS
          SELECT NRO_PAGINAS INTO V_NRO_PAGINAS
          FROM LIVROS 
          WHERE CODLIVRO = :NEW.CODLIVRO;

          -- ATUALIZA (ACUMULA) O CAMPO PAGINOMETRO NA TABELA DE USUARIOS
          UPDATE USUARIOS
          SET PAGINOMETRO = NVL(PAGINOMETRO,0) + V_NRO_PAGINAS
          WHERE CODUSUARIO = :NEW.CODUSUARIO;
    END IF;

END;
	



